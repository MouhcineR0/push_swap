#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*copy;
	size_t	i;

	copy = (char *) malloc(ft_strlen(src) + 1);
	if (!copy)
		return (0);
	i = 0;
	while (src[i])
	{
		copy[i] = src[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
